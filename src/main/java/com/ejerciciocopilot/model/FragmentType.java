package com.ejerciciocopilot.model;

/**
 * Enumeración que define los tipos de fragmentos que componen una excusa tech.
 */
public enum FragmentType {
    /**
     * Contexto: La situación en la que ocurrió el problema.
     */
    CONTEXTO,

    /**
     * Causa: La razón técnica por la que sucedió.
     */
    CAUSA,

    /**
     * Consecuencia: El resultado o impacto del problema.
     */
    CONSECUENCIA,

    /**
     * Recomendación: La solución sugerida para el futuro.
     */
    RECOMENDACION
}
